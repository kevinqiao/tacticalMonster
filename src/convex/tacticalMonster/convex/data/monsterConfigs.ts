/**
 * 计算成长率（基于基础属性）
 */
function calculateGrowthRates(baseHp: number, baseDamage: number, baseDefense: number, baseSpeed: number) {
    return {
        hp: Math.floor(baseHp * 0.15), // 每级增长15%基础HP
        damage: Math.floor(baseDamage * 0.10), // 每级增长10%基础伤害
        defense: Math.floor(baseDefense * 0.12), // 每级增长12%基础防御
        speed: Math.floor(baseSpeed * 0.05), // 每级增长5%基础速度
    };
}

/**
 * 生成怪物配置
 */
function createMonsterConfig(
    index: number,
    monster: typeof CAMEX_MONSTERS[0]
): MonsterConfig {
    const monsterId = `monster_${String(index + 1).padStart(3, "0")}`;

    return {
        monsterId,
        name: monster.name,
        rarity: monster.rarity,
        class: monster.class,
        race: monster.race,
        baseHp: monster.baseHp,
        baseDamage: monster.baseDamage,
        baseDefense: monster.baseDefense,
        baseSpeed: monster.baseSpeed,
        skills: [], // 技能配置后续添加
        growthRates: calculateGrowthRates(
            monster.baseHp,
            monster.baseDamage,
            monster.baseDefense,
            monster.baseSpeed
        ),
        assetPath: monster.assetPath,
        configVersion: 1,
    };
}

/**
 * 生成所有怪物配置
 */
export function generateMonsterConfigs(): MonsterConfig[] {
    return CAMEX_MONSTERS.map((monster, index) => createMonsterConfig(index, monster));
}

/**
 * 导出的怪物配置数据
 */
export const MONSTER_CONFIGS: Record<string, MonsterConfig> = (() => {
    const configs = generateMonsterConfigs();
    const configMap: Record<string, MonsterConfig> = {};

    configs.forEach((config) => {
        configMap[config.monsterId] = config;
    });

    return configMap;
})();

/**
 * 获取所有怪物配置数组
 */
export function getAllMonsterConfigs(): MonsterConfig[] {
    return Object.values(MONSTER_CONFIGS);
}

/**
 * 按稀有度获取怪物配置
 */
export function getMonsterConfigsByRarity(rarity: "Common" | "Rare" | "Epic" | "Legendary"): MonsterConfig[] {
    return getAllMonsterConfigs().filter((config) => config.rarity === rarity);
}
